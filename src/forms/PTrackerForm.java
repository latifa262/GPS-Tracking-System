    private void loadTrackers() {
        for (GPSTracker t : ts.findAll()) {
            listeTrackers.addItem(t);
        }
    }

    private void loadPositions() {
        model.setRowCount(0);
        for (Position p : positions) {
            model.addRow(new Object[]{
                p.getId(),
                p.getLatitude(),
                p.getLongitude(),
                p.getDate(),
                p.getGpsTracker()
            });

        }
    }
